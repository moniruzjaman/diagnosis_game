// Combine all issues
export const ALL_RICE_ISSUES: RiceIssue[] = [
  ...RICE_DISEASES,
  ...RICE_PESTS,
  ...RICE_DEFICIENCIES
];

// Helper function to get a random issue
export function getRandomIssue(): RiceIssue {
  return ALL_RICE_ISSUES[Math.floor(Math.random() * ALL_RICE_ISSUES.length)];
}

// Helper function to get issue by type
export function getIssuesByType(type: IssueType): RiceIssue[] {
  return ALL_RICE_ISSUES.filter(issue => issue.type === type);
}

// Helper function to get a random issue of specific type
export function getRandomIssueByType(type: IssueType): RiceIssue {
  const issues = getIssuesByType(type);
  return issues[Math.floor(Math.random() * issues.length)];
}

// Get random issue excluding specific IDs
export function getRandomIssueExcluding(excludeIds: string[]): RiceIssue {
  const filtered = ALL_RICE_ISSUES.filter(issue => !excludeIds.includes(issue.id));
  return filtered[Math.floor(Math.random() * filtered.length)];
}
